#include <iostream>

using namespace std;

int main() {
    int a = 24;
    int b = 25;

    cout << "Variabel A : " << a << endl;
    cout << "Alamat variabel A : " << &a << endl;

    cout << "Variabel B : " << b << endl;
    cout << "Alamat variabel B : " << &b << endl;

    return 0;
}

