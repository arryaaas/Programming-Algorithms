#include <iostream>

using namespace std;

int main() {
    float sisi, luas;

    cout << "Masukkan panjang sisi persegi : "; cin >> sisi;

    luas = sisi * sisi;

    cout << "Luas persegi adalah " << luas << " cm^2" << endl;

    return 0;
}

/* Menerima masukan sisi sebuah persegi, dan akan
mengeluarkan nilai luas persegi tersebut! */